package helpers;

public class GameData 
{
	private int higherScore;
	private int waveNumberScore;
	
	public void setHigherScore(int higherScore)
	{
		this.higherScore = higherScore;
	}
	public int getHigherScore()
	{
		return this.higherScore;
	}
	
	public void setWaveNumberScore(int waveNumberScore)
	{
		this.waveNumberScore = waveNumberScore;
	}
	public int getWaveNumberScore()
	{
		return waveNumberScore;
	}
}
